#include <stdio.h>


int a,b;

a = 5;
b = 3;
int main(void)
{
	printf("\변수 A에 저장된 숫자는 %d 입니다!\n", a);
	printf("\n변수 B에 저장된 숫자는 %d 입니다.\n", b);
	printf("\n변수 A와 B를 덧셈하면 %d 입니다.\n", a+b);
	printf("\n변수 A와 B를 곱셈하면 %d 입니다.\n", a*b);
	printf("\n변수 A와 B를 뺄셈하면 %d 입니다.\n", a-b);
	printf("\n변수 A와 B를 나누기하면 %d 입니다.\n", a/b);
	
}
